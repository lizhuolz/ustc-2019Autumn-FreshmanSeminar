function p = pmap(x1,flag,y1,shi)
%返回的值为相应的进球概率，在0到1之间
%判断是否进球有效时，大于等于0.5视为进球有效
%输入的四个参数对应draw函数返回的四个参数
%为加快运行速度，使用draw2函数替换draw函数，唯一的区别是不进行画图
x1=abs(x1);
if flag<0.0 && x1<3.66&&y1<2.44
    t=2.2012*x1*x1+2.4141*x1*x1*shi+0.6002*x1*x1*shi*shi+y1*y1*0.0912+y1*y1*shi*1.6799+y1*y1*shi*shi*7.8414-0.1711*y1-3.3602*y1*shi-15.7284*y1*shi*shi+0.0952+1.6648*shi+7.8307*shi*shi-5.0176*shi^4-19.8912*shi^3-21.7296*shi^2-3.9961*shi-0.2025;
    p=1/(1+exp(-t));
else
    p=0;
end 